import assert from "node:assert/strict";
import { mkdtemp } from "node:fs/promises";
import os from "node:os";
import path from "node:path";
import { createSessionService, credentialRole, sessionCookie } from "./session-auth.mjs";
import { consumeRateLimit } from "./security-rate-limit.mjs";

const dir = await mkdtemp(path.join(os.tmpdir(), "leos-session-"));
process.env.COMMAND_CENTER_DATA_PATH = path.join(dir, "state.json");
process.env.COMMAND_CENTER_SEED_PATH = path.join(dir, "missing.json");
const { JsonStore } = await import("./storage.mjs");
const env = { NODE_ENV:"production", COMMAND_CENTER_SESSION_SECRET:"9q-session-A7v!m2Zx#4Lp8Wc6Rk3Tn5Ys1Hd0", COMMAND_CENTER_OWNER_TOKEN:"9q-owner-A7v!m2Zx#4Lp8Wc6Rk3Tn5Ys1Hd0" };
const store = new JsonStore({ authSessions:[] });
await store.writeState({ authSessions:[] });
const sessions = createSessionService({ store, env });
assert.equal(credentialRole(env.COMMAND_CENTER_OWNER_TOKEN, env), "owner");
const created = await sessions.create("owner", { userAgent:"synthetic-test" });
const persisted = await store.readState();
assert.equal(persisted.authSessions.length, 1);
assert(!JSON.stringify(persisted).includes(created.token));
const cookie = sessionCookie(created.token, { env });
assert.match(cookie, /HttpOnly/);
assert.match(cookie, /Secure/);
assert.match(cookie, /SameSite=Lax/);
assert.match(cookie, /Max-Age=/);
const request = { headers:{ cookie:`leos_session=${encodeURIComponent(created.token)}`, "x-csrf-token":created.csrfToken } };
const actor = await sessions.authenticate(request);
assert.equal(actor.role, "owner");
assert.equal(sessions.csrfValid(request, actor), true);
assert.equal(sessions.csrfValid({ headers:{ cookie:request.headers.cookie } }, actor), false);
await sessions.revoke(request);
assert.equal(await sessions.authenticate(request), null);

const firstRate = await consumeRateLimit({ store, scope:"login", subject:"synthetic-client", limit:2, windowMs:60_000, now:1_000, secret:env.COMMAND_CENTER_SESSION_SECRET });
const secondRate = await consumeRateLimit({ store, scope:"login", subject:"synthetic-client", limit:2, windowMs:60_000, now:1_001, secret:env.COMMAND_CENTER_SESSION_SECRET });
const deniedRate = await consumeRateLimit({ store, scope:"login", subject:"synthetic-client", limit:2, windowMs:60_000, now:1_002, secret:env.COMMAND_CENTER_SESSION_SECRET });
assert.equal(firstRate.allowed, true);
assert.equal(secondRate.allowed, true);
assert.equal(deniedRate.allowed, false);
assert(deniedRate.retryAfterSeconds > 0);
console.log("session security tests passed");
